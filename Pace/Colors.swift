import UIKit

public extension UIColor {
	
	static func paceBrandColor() -> UIColor {
		return UIColor(fromHexString: "50E3C2")
	}
	
	static func greyBlackColor() -> UIColor {
		
		return UIColor(fromHexString: "485870")
	}
	
	static func darkBlack() -> UIColor {
		return UIColor(fromHexString: "14161B") 
	}
	
	static func offWhite() -> UIColor {
		return UIColor(fromHexString: "D2D7E4")
	}
	
	static func greyWhite() -> UIColor {
		return UIColor(fromHexString: "C9D5EB")
	}
	
	static func headerBlack() -> UIColor {
		return UIColor(fromHexString: "0C0E10")
	}
	
	static func darkerBlack() -> UIColor {
		return UIColor(fromHexString: "080A0B")
	}
	
	static func paceBackgroundBlack() -> UIColor {
		return UIColor(fromHexString: "0A0C0E")
	}
	
	static func closeBlack() -> UIColor {
		
		return UIColor(fromHexString: "050505")
	
	}
	
	//	Day Colors
	static func sunday() -> UIColor {
		return UIColor(fromHexString: "F71952")
	}
	
	static func monday() -> UIColor {
		return UIColor(fromHexString: "17D5FD")
	}
	
	static func tuesday() -> UIColor {
		return UIColor(fromHexString: "BB00FF")
	}
	
	static func wednesday() -> UIColor {
		return UIColor(fromHexString: "FAB904")
	}
	
	static func thursday() -> UIColor {
		return UIColor(fromHexString: "50E3C2")
	}
	
	static func friday() -> UIColor {
		return UIColor(fromHexString: "607D8B")
	}
	
	static func saturday() -> UIColor {
		return UIColor(fromHexString: "FF5E00")
	}
	

}

